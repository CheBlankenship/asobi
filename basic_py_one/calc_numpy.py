import numpy as np


# Calculate ELEMENT WISE

x = np.array([1.0, 2.0, 3.0])
y = np.array([2.0, 4.0, 6.0])

# Sum
sum_each_index = x + y
print(sum_each_index)

# Sub
subs_each_index = x - y
print(subs_each_index)

# Multi
multi_each_index = x * y
print(multi_each_index)

# Divide
divide_each_index = x / y
print(divide_each_index)
